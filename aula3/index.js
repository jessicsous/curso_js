// o console é utilizado para exibir informações na tela
console.log(); //  log é um método que está dentro do objeto console, (o ponto e virgula é opcional)
console.log('Jessica Silva'); // para exibir um texto é preciso envolve-lo entre aspas
console.log("Jessica Silva"); // console.log sempre quebra a linha na sainda também
console.log(`Jessica Silva`); // utilizado para template strings
console.log("jessica 'silva'"); //tudo entre aspas e craze é considerado string
console.log('jessica "silva"'); // string
console.log(`'Jessica' "silva"`); //string

console.log(123456); // numeros não precisam de aspas (number)
console.log(15.85); // numero de ponto flutuante (number em js)
console.log(25, 25.5, 'jessica silva')